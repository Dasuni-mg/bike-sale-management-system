    private void btnenterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnenterActionPerformed
        LoadElements();
        DBConnection.Connect();
        try
        {
            if(this.txtmid.getText().equals(""))
            {
                JOptionPane.showMessageDialog(this, "Model ID is required!");
            }
           
             else if(this.txtcid.getText().equals(""))
            {
                JOptionPane.showMessageDialog(this, "Customer ID is required!");
            }
              else if(this.txten.getText().equals(""))
            {
                JOptionPane.showMessageDialog(this, "Employee Name is required!");
            }
               else if(this.dtpp.getDate() == null)
            {
                JOptionPane.showMessageDialog(this, "Buying Date is required!");
            }

            else
            {
                Statement st=DBConnection.con.createStatement();
                st.executeUpdate("INSERT INTO BUY VALUES('"+modelid+"','"+customerid+"','"+buying_date+"','"+empname+"')");
                JOptionPane.showMessageDialog(this, "Record is inserted succesfully");
                DBConnection.con.close();
                LoadTable();
            }
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnenterActionPerformed

