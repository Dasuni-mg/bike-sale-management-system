    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        DBConnection.Connect();
        try
        {
            Statement st = DBConnection.con.createStatement();
            ResultSet res = st.executeQuery("SELECT*FROM BUY WHERE MODELID='"+this.txtmid.getText()+"'");
            while(res.next())
            {
                this.txtcid.setText(res.getString("CUSTOMERID"));
                this.txtmid.setText(res.getString("MODELID"));
                this.txten.setText(res.getString("EMPNAME"));
                this.dtpp.setDate(res.getDate("BUYING_DATE"));
            }
            DBConnection.con.close();

        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,ex.getMessage());

        }// TODO add your handling code here:
    }//GEN-LAST:event_btnsearchActionPerformed
      void LoadElements()
      {
      modelid=this.txtmid.getText();
      customerid=this.txtcid.getText();
      buying_date=this.dtpp.getDate();
      empname=this.txten.getText();
      
      
      }
      void LoadTable()
      {
      DefaultTableModel dtm=new DefaultTableModel();
    dtm.addColumn("Model ID");
    dtm.addColumn("Customer ID");
    dtm.addColumn("Buying_Date");
    dtm.addColumn("Employee Name");
    
    
    
    DBConnection.Connect();
    try
    {
    Statement st=DBConnection.con.createStatement();
    ResultSet rs=st.executeQuery("SELECT * FROM BUY");
    while(rs.next())
    {
    String MODELID = rs.getString("MODELID");
    String CUSTOMERID = rs.getString("CUSTOMERID");
    String EMPNAME = rs.getString("EMPNAME");
         
    Date BUYING_DATE = rs.getDate("BUYING_DATE");
    
    dtm.addRow(new String[]{MODELID,CUSTOMERID,EMPNAME});
    dtm.addRow(new Date[]{BUYING_DATE  });
    }
    this.tblp.setModel(dtm);
    DBConnection.con.close();
    }
    catch(SQLException ex)
            {
            JOptionPane.showMessageDialog(null,ex.getMessage());
            }
      
      }
